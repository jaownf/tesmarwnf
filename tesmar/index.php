document.addEventListener('DOMContentLoaded', function() {
    function adjustLayout() {
        const width = window.innerWidth;

        if (width <= 1024) {
            document.querySelector('header').style.flexDirection = 'column';
        } else {
            document.querySelector('header').style.flexDirection = 'row';
        }

        const cols = document.querySelectorAll('.col-4, .col-5, .col-6');
        cols.forEach(col => {
            if (width <= 768) {
                col.style.flex = '1 1 100%';
                col.style.maxWidth = '100%';
            } else if (width <= 1024) {
                col.style.flex = '1 1 calc(50% - 10px)';
                col.style.maxWidth = 'calc(50% - 10px)';
            } else {
                col.style.flex = '1 1 calc(33.33% - 20px)';
                col.style.maxWidth = 'calc(33.33% - 20px)';
            }
        });
    }

    window.addEventListener('resize', adjustLayout);
    adjustLayout();
});
